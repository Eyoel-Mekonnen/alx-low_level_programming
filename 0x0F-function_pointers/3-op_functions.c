#include "3-calc.h"


/**
 * op_add - adds two numbers
 * @a: first argument
 * @b: Second argument
 *
 * Return: sum of two numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts two numbers
 * @a: first argument
 * @b: second argument
 *
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies two number
 * @a: first argument
 * @b: second argument
 *
 * Return: the mult of two number
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two numbers
 * @a: first argument
 * @b: second argument
 *
 * Return: the division of two number
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo of two numbers
 * @a: first argument
 * @b: Second argument
 *
 * Return: Modulo of two numbers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
